<script lang="ts" setup>
import { NDescriptions, NDescriptionsItem, NDrawer, NDrawerContent, NImage, NTime } from 'naive-ui'
import { useProSampleLibraryFetch } from '@/composables'

const emit = defineEmits(['close'])
const show = ref(false)
const { proSampleLibraryDefaultData } = useProSampleLibraryFetch()
const formData = ref<any>({ ...proSampleLibraryDefaultData })
function doClose() {
  emit('close')
  show.value = false
  formData.value = { ...proSampleLibraryDefaultData }
}

function doOpen(row: any) {
  show.value = true
  formData.value = row
}
defineExpose({
  doOpen,
})
</script>

<template>
  <NDrawer v-model:show="show" placement="right" width="800" @after-leave="doClose">
    <NDrawerContent title="详情">
      <NDescriptions label-placement="left" bordered :column="1">
          <NDescriptionsItem label="主键">
            {{ formData.id }}
          </NDescriptionsItem>
          <NDescriptionsItem label="用户ID">
            {{ formData.userId }}
          </NDescriptionsItem>
          <NDescriptionsItem label="题目ID">
            {{ formData.problemId }}
          </NDescriptionsItem>
          <NDescriptionsItem label="提交ID">
            {{ formData.submitId }}
          </NDescriptionsItem>
          <NDescriptionsItem label="提交时间">
            <NTime :time="Number(formData.submitTime)" />
          </NDescriptionsItem>
          <NDescriptionsItem label="编程语言">
            {{ formData.language }}
          </NDescriptionsItem>
          <NDescriptionsItem label="源代码">
            {{ formData.code }}
          </NDescriptionsItem>
          <NDescriptionsItem label="源代码长度">
            {{ formData.codeLength }}
          </NDescriptionsItem>
          <NDescriptionsItem label="访问次数">
            {{ formData.accessCount }}
          </NDescriptionsItem>
            <NDescriptionsItem label="创建时间">
              <NTime :time="Number(formData.createTime)" />
            </NDescriptionsItem>
            <NDescriptionsItem label="创建者">
              {{ formData.createUserName }}
            </NDescriptionsItem>
            <NDescriptionsItem label="更新时间">
              <NTime :time="Number(formData.updateTime)" />
            </NDescriptionsItem>
            <NDescriptionsItem label="更新人">
              {{ formData.updateUserName }}
            </NDescriptionsItem>
      </NDescriptions>
      <template #footer>
        <NSpace align="center" justify="end">
          <NButton @click="doClose">
            <template #icon>
              <IconParkOutlineClose />
            </template>
            关闭
          </NButton>
        </NSpace>
      </template>
    </NDrawerContent>
  </NDrawer>
</template>

